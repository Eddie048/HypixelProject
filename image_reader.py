# takes in a small 2D boolean array and returns the character it looks like, if any
def get_char(char_arr):
    for val in range(len(char_code)):
        if char_arr == char_code[val]:
            return char_key[val]

    return None


# takes in a long 2D boolean array and returns the string it looks like
def read_string(str_arr):

    blank_col = [0, 0, 0, 0, 0, 0, 0, 0]

    index = 0
    prev_blank_index = -1

    result_ign = ""

    for col in str_arr:

        if col == blank_col:
            if index - prev_blank_index > 1:

                temp_char = get_char(str_arr[prev_blank_index + 1: index])

                if temp_char is not None:
                    result_ign += temp_char

            prev_blank_index = index

        index += 1

    return result_ign
